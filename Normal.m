function [M2, P2P1, T2T1, P02P01] = Normal(M1,g)
    M2 = sqrt((1+ 0.5*(g-1)*M1^2)/(g*M1^2 - 0.5*(g-1)));
    P2P1 = 1 + 2*g*(M1^2 - 1)/(g+1);
    T2T1 = (1 + 2*g*(M1^2 - 1)/(g+1))*((2 + (g-1)*M1^2)/((g+1)*M1^2));

    A = 2/((g+1)*(g*M1^2 - 0.5*(g-1))^(1/(g-1)));
    B = (0.5*(g+1)*M1)^2;
    C = 1 + 0.5*(g-1)*M1^2;
    P02P01 = A*(B/C)^(g/(g-1));
    
end
       

    